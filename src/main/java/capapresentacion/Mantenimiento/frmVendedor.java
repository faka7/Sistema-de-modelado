    private void btnEscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscribirActionPerformed
        String nombre = txtCodigoVendedor.getText();
        String apellidos = txtNombreVendedor.getText();
        String dni = txtCelularVendedor.getText();
        String horario = txthorario.getText();

        Vendedor vendedor= new Vendedor();
        vendedor.setNombre(nombre);
        vendedor.setApellidos(apellidos);
        vendedor.setDni(dni);
        vendedor.setHorario(dni);
    }//GEN-LAST:event_btnEscribirActionPerformed

